package net.minecraft.world.entity.decoration;

public class PaintingVariant {
	private final int width;
	private final int height;

	public PaintingVariant(int i, int j) {
		this.width = i;
		this.height = j;
	}

	public int getWidth() {
		return this.width;
	}

	public int getHeight() {
		return this.height;
	}
}
