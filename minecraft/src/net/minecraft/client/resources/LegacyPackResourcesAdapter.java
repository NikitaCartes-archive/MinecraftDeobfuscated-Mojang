	private static ResourceLocation toMetaLocation(ResourceLocation resourceLocation) {
		return new ResourceLocation(resourceLocation.getNamespace(), resourceLocation.getPath() + ".mcmeta");
	}

	public LegacyPackResourcesAdapter(PackResources packResources, Map<ResourceLocation, ResourceLocation> map) {
		this.source = packResources;
		this.patches = map;
	}

	private ResourceLocation map(ResourceLocation resourceLocation) {
		return (ResourceLocation)this.patches.getOrDefault(resourceLocation, resourceLocation);
	}

	@Override
	public InputStream getRootResource(String string) throws IOException {
		return this.source.getRootResource(string);
	}

	@Override
	public InputStream getResource(PackType packType, ResourceLocation resourceLocation) throws IOException {
		return this.source.getResource(packType, this.map(resourceLocation));
	}

	@Override
	public Collection<ResourceLocation> getResources(PackType packType, String string, String string2, Predicate<ResourceLocation> predicate) {
		return Collections.emptyList();
	}

	@Override
	public boolean hasResource(PackType packType, ResourceLocation resourceLocation) {
		return this.source.hasResource(packType, this.map(resourceLocation));
	}

	@Override
	public Set<String> getNamespaces(PackType packType) {
		return this.source.getNamespaces(packType);
	}

	@Nullable
	@Override
	public <T> T getMetadataSection(MetadataSectionSerializer<T> metadataSectionSerializer) throws IOException {
		return this.source.getMetadataSection(metadataSectionSerializer);
	}

	@Override
	public String getName() {
		return this.source.getName();
	}

	@Override
	public void close() {
		this.source.close();
	}
